    @Override
    protected ReturnCode_t onExecute(int ec_id) {
    	if(m_inIn.isNew()) {
    		m_inIn.read();
    		float d = m_in.v.data;
    		if(d < m_threshold.value) {
    			this.get_context(0).deactivate_component(this.getObjRef());
    		}
    	}
    	return super.onExecute(ec_id);
    }

